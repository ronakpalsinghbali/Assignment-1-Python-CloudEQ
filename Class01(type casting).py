# Type Casting 

# 1. From String to Integer

a = "25"
print(a)
print(type(a))


a = int(a)
print(type(a))
print(a)


# 2. From Integer to Float
print("\n")


b = 35
print(b)
print(type(b))

b = float(b)
print(type(b))
print(b)


# 3. From Float to String
print("\n")


c = 52.50
print(c)
print(type(c))


c = str(c)
print(type(c))
print(c)
